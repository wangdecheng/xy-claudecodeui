#!/usr/bin/env bash
# scripts/diff-onsite-ui-vs-prototype.sh
#
# Closure gate: 对比 prototype HTML 与 UI 实现,产出 prototype-diff-report.md。
# 任何 FAIL 项必须先修才能 closure / merge。
#
# 使用:
#   ./scripts/diff-onsite-ui-vs-prototype.sh             # 实际跑(写入 prototype-diff-report.md)
#   ./scripts/diff-onsite-ui-vs-prototype.sh --dry-run   # 输出 checklist 模板,不实际检测
#
# Exit codes:
#   0  全部 PASS
#   1  至少 1 条 FAIL
#   2  prototype 文件不存在 / 不可读

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PROTOTYPE="$REPO_ROOT/design-prototypes/customer-onsite-analysis/index.html"
SRC_DIR="$REPO_ROOT/src/components/onsite-analysis"
SHARED_TYPES="$REPO_ROOT/shared/onsite-types.ts"
REPORT="$REPO_ROOT/prototype-diff-report.md"
DRY_RUN="false"

if [[ "${1:-}" == "--dry-run" ]]; then
  DRY_RUN="true"
fi

# ──── Pre-flight ─────────────────────────────────────────────────────────────
if [[ ! -f "$PROTOTYPE" ]]; then
  echo "ERROR: prototype file not found: $PROTOTYPE" >&2
  exit 2
fi

# ──── Checklist ──────────────────────────────────────────────────────────────
# 17 条 prototype field alignment checklist
# 用 NUL 分隔字段避免与内容冲突;每条:"REQ|Description|Pattern|Files"
CHECKS=(
  $'REQ-1.5\tDate picker\tdata-testid="onsite-date-input"\t'"$SRC_DIR/NewIssueWizard.tsx"$'\t'"$SHARED_TYPES"
  $'REQ-1.6\tModal subtitle\tdata-testid="onsite-wizard-subtitle"\t'"$SRC_DIR/NewIssueWizard.tsx"
  $'REQ-1.7\tdz-note amber hint\tdata-testid="onsite-dz-note"\t'"$SRC_DIR/LogUploader.tsx"
  $'REQ-1.8\tCustomer label suffix\tc\\.branch === null\thasInline\thasInline.*branch\t'"$SRC_DIR/CustomerSelect.tsx"
  $'REQ-1.10\tDatabase other option\tDATABASE_KIND_OTHER\tdatabaseOther\t'"$SRC_DIR/DatabaseSelect.tsx"
  $'REQ-1.11\tESC close\te\\.key === .Escape.\taddEventListener..keydown\t'"$SRC_DIR/NewIssueWizard.tsx"
  $'REQ-1.12\tDescription field\tdata-testid="onsite-description-input"\tdescriptionRequired\t'"$SRC_DIR/NewIssueWizard.tsx"$'\t'"$SHARED_TYPES"
  $'REQ-1.13\tAlways-visible dropzone\tLogUploader problemId.\\{null\\}\t'"$SRC_DIR/NewIssueWizard.tsx"
  $'REQ-4.5\tMessage avatar\tonsite-avatar-.\thsl.14_55.55.\t'"$SRC_DIR/OnsiteChatStream.tsx"
  $'REQ-4.6\tmsg-role label\tdata-testid="onsite-msg-role"\t现场反馈\t'"$SRC_DIR/OnsiteChatStream.tsx"
  $'REQ-4.7\tComposer hint\tdata-testid="onsite-composer-hint"\tcomposerHint\t'"$SRC_DIR/OnsiteChatStream.tsx"
  $'REQ-4.8\tWorkflow placeholder\tonsite:chat.placeholder\t'"$SRC_DIR/OnsiteChatStream.tsx"
  $'REQ-4.10\tThree-color highlight\tHL_HIT\tOK_HIT\tZERO_HIT\t'"$SRC_DIR/cards/EvidenceCard.tsx"
  $'REQ-2.6\tBusiness-phase groups\tBUSINESS_PHASES\tonsite-group-active\t'"$SRC_DIR/IssueListSidebar.tsx"
  $'REQ-2.7\tFull-width new issue button\tonsite-new-issue-button\tw-full\t'"$SRC_DIR/IssueListSidebar.tsx"
  $'D-10\tDescription backward compat\tdescription: string\t'"$SHARED_TYPES"
  $'D-11\tPrototype exists\t'  # 用空 pattern;下面 file_exists 检查特殊处理
)

pass=0
fail=0
results=()

check_pattern() {
  local pattern="$1"; shift
  local files="$@"
  local file
  for file in $files; do
    if [[ -f "$file" ]] && grep -qE "$pattern" "$file"; then
      echo "$file:$(grep -nE "$pattern" "$file" | head -1 | cut -d: -f1)"
      return 0
    fi
  done
  return 1
}

for entry in "${CHECKS[@]}"; do
  IFS=$'\t' read -r req desc field3 field4 field5 field6 <<< "$entry"
  # 兼容 1~3 个 pattern
  patterns=()
  files="$field4"
  if [[ -n "$field3" && -z "$field4" ]]; then
    files=""
    patterns=("$field3")
  else
    [[ -n "$field3" ]] && patterns+=("$field3")
    if [[ -n "$field4" && "$field4" == *"/Users"* ]]; then
      # field4 是文件路径
      files="$field4 ${field5:-} ${field6:-}"
    else
      [[ -n "$field4" ]] && patterns+=("$field4")
      files="$field5 ${field6:-}"
    fi
  fi

  if [[ "$DRY_RUN" == "true" ]]; then
    results+=("| $req | $desc | (dry-run) | — |")
    continue
  fi

  # D-11 特殊处理:文件存在检查
  if [[ "$req" == "D-11" ]]; then
    if [[ -f "$PROTOTYPE" ]]; then
      pass=$((pass + 1))
      results+=("| $req | $desc | PASS | prototype exists at \`$PROTOTYPE\` |")
    else
      fail=$((fail + 1))
      results+=("| $req | $desc | **FAIL** | prototype file missing |")
    fi
    continue
  fi

  found=""
  for pat in "${patterns[@]}"; do
    evidence=$(check_pattern "$pat" $files 2>/dev/null || true)
    if [[ -n "$evidence" ]]; then
      found="$evidence"
      break
    fi
  done

  if [[ -n "$found" ]]; then
    pass=$((pass + 1))
    results+=("| $req | $desc | PASS | \`$found\` |")
  else
    fail=$((fail + 1))
    pattern_display=$(IFS='/'; echo "${patterns[*]}")
    results+=("| $req | $desc | **FAIL** | 无匹配 — 模式 \`$pattern_display\` 在 ${files} 中找不到 |")
  fi
done

total=${#CHECKS[@]}

# ──── 输出报告 ────────────────────────────────────────────────────────────────
{
  echo "# Prototype Diff Report"
  echo
  echo "Generated by \`scripts/diff-onsite-ui-vs-prototype.sh\` at $(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo
  echo "Source: \`$PROTOTYPE\`"
  echo "Implementation: \`$SRC_DIR\`"
  echo
  echo "## Summary"
  echo
  echo "- Total: $total"
  echo "- PASS: $pass"
  echo "- FAIL: $fail"
  echo
  echo "## Checklist"
  echo
  echo "| Req | Description | Status | Evidence |"
  echo "|-----|-------------|--------|----------|"
  for r in "${results[@]}"; do
    echo "$r"
  done
  echo
  if [[ "$fail" -gt 0 ]]; then
    echo "## Verdict"
    echo
    echo "❌ **BLOCKED** — $fail 项 FAIL,必须先修才能 closure。"
  else
    echo "## Verdict"
    echo
    echo "✅ **PASS** — 全部 $pass 条对齐 prototype。"
  fi
} > "$REPORT"

cat "$REPORT"

if [[ "$DRY_RUN" == "true" ]]; then
  exit 0
fi

if [[ "$fail" -gt 0 ]]; then
  exit 1
fi
exit 0
